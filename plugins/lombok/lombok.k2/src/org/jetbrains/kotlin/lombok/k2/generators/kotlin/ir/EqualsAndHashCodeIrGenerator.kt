package org.jetbrains.kotlin.lombok.k2.generators.kotlin.ir

import org.jetbrains.kotlin.ir.UNDEFINED_OFFSET
import org.jetbrains.kotlin.ir.builders.*
import org.jetbrains.kotlin.ir.declarations.*
import org.jetbrains.kotlin.ir.expressions.IrExpression
import org.jetbrains.kotlin.ir.expressions.IrStatementOrigin
import org.jetbrains.kotlin.ir.expressions.impl.IrCallImpl
import org.jetbrains.kotlin.ir.expressions.impl.IrCallImplWithShape
import org.jetbrains.kotlin.ir.expressions.impl.IrGetValueImpl
import org.jetbrains.kotlin.ir.symbols.UnsafeDuringIrConstructionAPI
import org.jetbrains.kotlin.ir.types.classOrNull
import org.jetbrains.kotlin.ir.util.findDeclaration
import org.jetbrains.kotlin.ir.util.functions
import org.jetbrains.kotlin.ir.util.isInterface
import org.jetbrains.kotlin.ir.util.isNullable
import org.jetbrains.kotlin.lombok.k2.generators.EqualsAndHashCodeGenerator
import org.jetbrains.kotlin.lombok.k2.generators.EqualsAndHashCodeGenerator.Companion.EQUALS_NAME
import org.jetbrains.kotlin.lombok.k2.generators.EqualsAndHashCodeGenerator.Companion.HASHCODE_NAME
import org.jetbrains.kotlin.lombok.k2.generators.EqualsAndHashCodeGeneratorKey
import org.jetbrains.kotlin.util.OperatorNameConventions
import org.jetbrains.kotlin.utils.addToStdlib.runIf

/**
 * Fills the bodies with `equals(other: Any?): Boolean` and `hashCode(): Int` previously generated by
 * [EqualsAndHashCodeGenerator]. Mirrors the body shape produced by Kotlin's data class members
 * generator — enough for "Lombok-style" semantics on plain classes, data classes, and objects.
 */
object EqualsAndHashCodeIrBodyBuilder : IrBodyBuilder<EqualsAndHashCodeGeneratorKey>() {
    override fun IrBlockBodyBuilder.build(
        key: EqualsAndHashCodeGeneratorKey,
        declaration: IrSimpleFunction,
    ) {
        val irClass = declaration.parent as IrClass
        val thisParam = declaration.dispatchReceiverParameter!!

        when (declaration.name) {
            EQUALS_NAME -> {
                val otherParam = declaration.parameters.single { it.kind == IrParameterKind.Regular }
                buildEqualsBody(irClass, key, thisParam, otherParam)
            }
            HASHCODE_NAME -> {
                buildHashCodeBody(irClass, key, thisParam)
            }
        }
    }

    private fun IrBlockBodyBuilder.buildEqualsBody(
        irClass: IrClass,
        key: EqualsAndHashCodeGeneratorKey,
        thisParam: IrValueParameter,
        otherParam: IrValueParameter,
    ) {
        +irIfThenReturnTrue(irEqeqeq(irGetThis(thisParam), irGetOther(otherParam)))
        +irIfThenReturnFalse(irNotIs(irGetOther(otherParam), irClass.defaultTypeForLombok()))

        val superEquals = runIf(key.callSuper) { buildSuperEqualsCall(irClass, thisParam, otherParam, context) }
        if (superEquals != null) {
            +irIfThenReturnFalse(
                primitiveBooleanNot(superEquals)
            )
        }

        val included: List<IrProperty> = extractIncludedProperties(key, irClass)

        if (included.isEmpty()) {
            +irReturnTrue()
            return
        }

        val otherCast = irTemporary(
            irImplicitCast(irGetOther(otherParam), irClass.defaultTypeForLombok()),
            nameHint = "other_with_cast",
        )

        for (property in included) {
            val thisProp = irGetPropertyValue(irGetThis(thisParam), property)
            val otherProp = irGetPropertyValue(irGet(otherCast), property)
            +irIfThenReturnFalse(
                primitiveBooleanNot(
                    irEquals(thisProp, otherProp, origin = IrStatementOrigin.EXCLEQ)
                )
            )
        }

        +irReturnTrue()
    }

    private fun IrBlockBodyBuilder.buildHashCodeBody(
        irClass: IrClass,
        key: EqualsAndHashCodeGeneratorKey,
        thisParam: IrValueParameter,
    ) {
        val includedProperties = extractIncludedProperties(key, irClass)

        val superHashCode = runIf(key.callSuper) { buildSuperHashCodeCall(irClass, thisParam, context) }

        if (includedProperties.isEmpty()) {
            +irReturn(superHashCode ?: irInt(0))
            return
        }

        if (superHashCode == null && includedProperties.size == 1) {
            +irReturn(hashCodeOfProperty(thisParam, includedProperties.single()))
            return
        }

        val initial: IrExpression = superHashCode ?: irInt(1)
        val resultVar = irTemporary(initial, nameHint = "result", isMutable = true)

        for (property in includedProperties) {
            val shifted = IrCallImplWithShape(
                startOffset = startOffset,
                endOffset = endOffset,
                type = context.irBuiltIns.intType,
                symbol = context.irBuiltIns.intTimesSymbol,
                typeArgumentsCount = 0,
                valueArgumentsCount = 1,
                contextParameterCount = 0,
                hasDispatchReceiver = true,
                hasExtensionReceiver = false,
            ).apply {
                arguments[0] = irGet(resultVar)
                arguments[1] = irInt(31)
            }
            val sum = IrCallImplWithShape(
                startOffset = startOffset,
                endOffset = endOffset,
                type = context.irBuiltIns.intType,
                symbol = context.irBuiltIns.intPlusSymbol,
                typeArgumentsCount = 0,
                valueArgumentsCount = 1,
                contextParameterCount = 0,
                hasDispatchReceiver = true,
                hasExtensionReceiver = false,
            ).apply {
                arguments[0] = shifted
                arguments[1] = hashCodeOfProperty(thisParam, property)
            }
            +irSet(resultVar.symbol, sum)
        }

        +irReturn(irGet(resultVar))
    }

    @OptIn(UnsafeDuringIrConstructionAPI::class)
    private fun extractIncludedProperties(
        key: EqualsAndHashCodeGeneratorKey,
        irClass: IrClass,
    ): List<IrProperty> = key.propertyInfos.mapNotNull { info ->
        val property = irClass.findDeclaration<IrProperty> { it.name == info.propertyName }
        runIf(property != null && (property.backingField != null || !info.ignoreWithoutBackingField)) { property }
    }

    private fun IrBlockBodyBuilder.hashCodeOfProperty(
        thisParam: IrValueParameter,
        property: IrProperty,
    ): IrExpression {
        val value = irGetPropertyValue(irGetThis(thisParam), property)
        val type = property.backingField?.type ?: property.getter?.returnType
        val isNullable = type?.isNullable() == true
        return if (isNullable) {
            irIfNull(
                context.irBuiltIns.intType,
                value,
                irInt(0),
                callHashCodeOn(irGetPropertyValue(irGetThis(thisParam), property)),
            )
        } else {
            callHashCodeOn(value)
        }
    }

    @OptIn(UnsafeDuringIrConstructionAPI::class)
    private fun IrBlockBodyBuilder.callHashCodeOn(receiver: IrExpression): IrExpression {
        // Use Any.hashCode — reliable cross-platform regardless of the property's static type.
        val anyHashCode = context.irBuiltIns.anyClass.owner.functions
            .single { it.name == HASHCODE_NAME && it.parameters.singleOrNull { p -> p.kind == IrParameterKind.DispatchReceiver } != null }
        return IrCallImpl(
            startOffset = startOffset,
            endOffset = endOffset,
            type = context.irBuiltIns.intType,
            symbol = anyHashCode.symbol,
            typeArgumentsCount = 0,
        ).apply {
            arguments[0] = receiver
        }
    }

    private fun IrBlockBodyBuilder.irGetPropertyValue(receiver: IrExpression, property: IrProperty): IrExpression {
        return irCall(property.getter!!.symbol).apply {
            arguments[0] = receiver
        }
    }

    private fun irGetThis(thisParam: IrValueParameter): IrExpression =
        IrGetValueImpl(UNDEFINED_OFFSET, UNDEFINED_OFFSET, thisParam.type, thisParam.symbol)

    private fun irGetOther(otherParam: IrValueParameter): IrExpression =
        IrGetValueImpl(UNDEFINED_OFFSET, UNDEFINED_OFFSET, otherParam.type, otherParam.symbol)

    private fun IrBlockBodyBuilder.primitiveBooleanNot(arg: IrExpression): IrExpression =
        IrCallImplWithShape(
            startOffset = startOffset,
            endOffset = endOffset,
            type = context.irBuiltIns.booleanType,
            symbol = context.irBuiltIns.booleanNotSymbol,
            typeArgumentsCount = 0,
            valueArgumentsCount = 0,
            contextParameterCount = 0,
            hasDispatchReceiver = true,
            hasExtensionReceiver = false,
        ).apply {
            arguments[0] = arg
        }

    @OptIn(UnsafeDuringIrConstructionAPI::class)
    private fun buildSuperEqualsCall(
        irClass: IrClass,
        thisParam: IrValueParameter,
        otherParam: IrValueParameter,
        context: IrGeneratorContext,
    ): IrExpression? {
        val superClass = irClass.nonAnyNonInterfaceSuperclass(context) ?: return null
        val superEquals = superClass.functions.firstOrNull {
            it.name == OperatorNameConventions.EQUALS &&
                    it.parameters.count { p -> p.kind == IrParameterKind.Regular } == 1 &&
                    it.parameters.singleOrNull { p -> p.kind == IrParameterKind.DispatchReceiver } != null
        } ?: return null

        return IrCallImpl(
            startOffset = UNDEFINED_OFFSET,
            endOffset = UNDEFINED_OFFSET,
            type = superEquals.returnType,
            symbol = superEquals.symbol,
            typeArgumentsCount = 0,
            superQualifierSymbol = superClass.symbol,
        ).apply {
            arguments[0] = IrGetValueImpl(UNDEFINED_OFFSET, UNDEFINED_OFFSET, thisParam.type, thisParam.symbol)
            arguments[1] = IrGetValueImpl(UNDEFINED_OFFSET, UNDEFINED_OFFSET, otherParam.type, otherParam.symbol)
        }
    }

    @OptIn(UnsafeDuringIrConstructionAPI::class)
    private fun buildSuperHashCodeCall(irClass: IrClass, thisParam: IrValueParameter, context: IrGeneratorContext): IrExpression? {
        val superClass = irClass.nonAnyNonInterfaceSuperclass(context) ?: return null
        val superHashCode = superClass.functions.firstOrNull {
            it.name == HASHCODE_NAME &&
                    it.parameters.count { p -> p.kind == IrParameterKind.Regular } == 0 &&
                    it.parameters.singleOrNull { p -> p.kind == IrParameterKind.DispatchReceiver } != null
        } ?: return null

        return IrCallImpl(
            startOffset = UNDEFINED_OFFSET,
            endOffset = UNDEFINED_OFFSET,
            type = superHashCode.returnType,
            symbol = superHashCode.symbol,
            typeArgumentsCount = 0,
            superQualifierSymbol = superClass.symbol,
        ).apply {
            arguments[0] = IrGetValueImpl(UNDEFINED_OFFSET, UNDEFINED_OFFSET, thisParam.type, thisParam.symbol)
        }
    }

    @OptIn(UnsafeDuringIrConstructionAPI::class)
    private fun IrClass.nonAnyNonInterfaceSuperclass(context: IrGeneratorContext): IrClass? =
        superTypes.firstNotNullOfOrNull { type -> type.classOrNull?.owner?.takeIf { !it.isInterface } }
            ?.takeIf { it.symbol != context.irBuiltIns.anyClass }

    private fun IrClass.defaultTypeForLombok() = thisReceiver!!.type
}
