import org.junit.jupiter.api.Tag

@Tag("custom-first-stage")
open class AbstractCustomWasmJsCompilerFirstStageTest(val testDataRoot: String = "compiler/testData/codegen/") :
    AbstractKotlinCompilerWithTargetBackendTest(TargetBackend.WASM_JS) {

    override fun configure(builder: TestConfigurationBuilder) = with(builder) {
        globalDefaults {
            frontend = FrontendKinds.FIR
            targetPlatform = WasmPlatforms.wasmJs
            dependencyKind = DependencyKind.Binary
        }

        useConfigurators(
            ::CommonEnvironmentConfigurator,
            ::WasmFirstStageEnvironmentConfigurator.bind(WasmTarget.JS),
            // And this configurator is necessary to relax the second compilation stage, since the old compiler could produce IR
            // which would not pass new improved IR validation rules
            ::CustomWebCompilerSecondStageEnvironmentConfigurator,
        )

        useAdditionalSourceProviders(
            ::CoroutineHelpersSourceFilesProvider,
            ::AdditionalDiagnosticsSourceFilesProvider,
        )

        facadeStep(::CustomWebCompilerFirstStageFacade)

        useAfterAnalysisCheckers(
            // Suppress all tests that have not been successfully compiled by the first stage.
            // And the limited number of tests where KLIBs generated by a specific compiler version
            // are known to have some problems that cause crash on the second stage.
            ::CustomKlibCompilerFirstStageTestSuppressor.bind(customWasmJsCompilerSettings.defaultLanguageVersion),

            // Suppress all tests that failed on the second stage if they are anyway marked as "IGNORE_BACKEND*".
            ::CustomKlibCompilerTestSuppressor,
        )

        commonConfigurationForWasmSecondStageTest(
            pathToTestDir = testDataRoot,
            testGroupOutputDirPrefix = this@AbstractCustomWasmJsCompilerFirstStageTest::class.java.simpleName +
                    customWasmJsCompilerSettings.defaultLanguageVersion,
        )
        useConfigurators(
            ::WasmSecondStageEnvironmentConfigurator.bind(WasmTarget.JS),
        )
        facadeStep(::WasmBackendFacade)

        wasmArtifactsHandlersStep {
            useHandlers(::WasmBoxRunner)
            useHandlers(::WasmIrHandler)
            useHandlers(::WasmDtsHandler)
        }
    }
}
