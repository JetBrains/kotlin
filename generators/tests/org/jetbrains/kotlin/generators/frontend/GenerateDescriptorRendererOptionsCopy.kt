import com.intellij.openapi.util.io.FileUtil
import org.jetbrains.kotlin.generators.util.GeneratorsFileUtil.writeFileIfContentChanged
import org.jetbrains.kotlin.renderer.DescriptorRendererOptionsImpl
import org.jetbrains.kotlin.utils.Printer
import java.io.File
import kotlin.reflect.full.memberProperties
import kotlin.reflect.jvm.isAccessible

object GenerateDescriptorRendererOptionsCopy {
    val DEST_FILE: File = File("core/descriptors/src/org/jetbrains/kotlin/renderer/DescriptorRendererOptionsCopy.kt")

    fun generate(): String {
        val sb = StringBuilder()
        val p = Printer(sb)

        p.println(FileUtil.loadFile(File("license/COPYRIGHT_HEADER.txt")))
        p.println()
        p.println("package org.jetbrains.kotlin.renderer")
        p.println()
        p.println("/** This file is generated by {@link \"org.jetbrains.kotlin.generators.frontend.GenerateDescriptorRendererOptionsCopy\"}. DO NOT MODIFY MANUALLY */")
        p.println("@Suppress(\"DuplicatedCode\")")
        p.println("internal fun DescriptorRendererOptionsImpl.copy(): DescriptorRendererOptionsImpl {")
        p.pushIndent()

        p.println("val copy = DescriptorRendererOptionsImpl()")
        val stub = DescriptorRendererOptionsImpl()
        for (property in DescriptorRendererOptionsImpl::class.memberProperties) {
            property.isAccessible = true
            if (property.getDelegate(stub) == null) continue
            check(!property.name.startsWith("is")) { "Fields named is* are not supported here yet: $property" }
            p.println("copy.${property.name} = ${property.name}")
        }
        p.println("return copy")
        p.popIndent()
        p.println("}")

        return sb.toString()
    }

    @JvmStatic
    fun main(args: Array<String>) {
        writeFileIfContentChanged(DEST_FILE, generate())
    }
}
