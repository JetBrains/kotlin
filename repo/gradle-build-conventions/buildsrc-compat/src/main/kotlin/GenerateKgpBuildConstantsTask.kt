import org.gradle.api.Action
import org.gradle.api.DefaultTask
import org.gradle.api.Project
import org.gradle.api.file.DirectoryProperty
import org.gradle.api.file.RegularFileProperty
import org.gradle.api.provider.Property
import org.gradle.api.tasks.*
import org.gradle.kotlin.dsl.register
import org.gradle.language.base.plugins.LifecycleBasePlugin
import org.gradle.work.DisableCachingByDefault
import org.gradle.work.NormalizeLineEndings

/**
 * Generates a Kotlin source file with constant values.
 * Used to align build-time variables with those used by KGP at runtime (e.g. dependency versions).
 */
@DisableCachingByDefault(because = "Not worth caching, simple file generation.")
abstract class GenerateKgpBuildConstantsTask internal constructor() : DefaultTask() {

    @get:OutputDirectory
    abstract val outputDir: DirectoryProperty

    @get:Input
    abstract val defaultYarnVersion: Property<String>

    /**
     * A file containing the copyright header at the top of the generated file.
     */
    @get:InputFile
    @get:PathSensitive(PathSensitivity.NONE)
    @get:NormalizeLineEndings
    abstract val copyrightHeader: RegularFileProperty

    @TaskAction
    protected fun action() {
        val copyrightHeader = copyrightHeader.get().asFile.readText()

        val buildConstants = outputDir.get().asFile.resolve("KgpBuildConstants.kt")
        buildConstants.writeText(
            """
            |${copyrightHeader}
            |package org.jetbrains.kotlin.internal
            |
            |// Generated by task $path - do not modify
            |internal object KgpBuildConstants {
            |    const val DEFAULT_YARN_VERSION = "${defaultYarnVersion.get()}"
            |}
            |""".trimMargin()
        )
    }

    companion object {
        private const val TASK_NAME = "generateKgpBuildConstants"

        fun Project.registerGenerateKgpBuildConstantsTask(
            configure: Action<GenerateKgpBuildConstantsTask>,
        ): TaskProvider<GenerateKgpBuildConstantsTask> =
            tasks.register(TASK_NAME, GenerateKgpBuildConstantsTask::class) {
                group = LifecycleBasePlugin.BUILD_GROUP
                description = "Generates a Kotlin source file with constant values. " +
                        "Used to align build-time variables with those used by KGP at runtime (e.g. dependency versions)."

                outputDir.convention(objects.directoryProperty().fileValue(temporaryDir))

                copyrightHeader.convention(
                    layout.settingsDirectory.file("license/COPYRIGHT_HEADER.txt")
                )

                configure.execute(this)
            }
    }
}
