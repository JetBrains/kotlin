// Primitives

interface Primitives {
    fun int(i: Int): Int

    val nullableBool: Boolean?
    val nullableByte: Byte?
    val nullableShort: Short?
    val nullableChar: Char?
    val nullableInt: Int?
    val nullableLong: Long?
    val nullableFloat: Float?
    val nullableDouble: Double?

    val bool: Boolean
    val byte: Byte
    val short: Short
    val char: Char
    val int: Int
    val long: Long
    val float: Float
    val double: Double
}

