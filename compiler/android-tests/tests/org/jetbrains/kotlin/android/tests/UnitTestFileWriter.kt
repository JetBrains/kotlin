package org.jetbrains.kotlin.android.tests

import com.intellij.openapi.util.io.FileUtil
import com.intellij.openapi.util.text.StringUtil
import org.jetbrains.kotlin.generators.util.TestGeneratorUtil.getMainClassName
import org.jetbrains.kotlin.name.FqName
import org.jetbrains.kotlin.name.NameUtils
import org.jetbrains.kotlin.utils.Printer
import java.io.File
import java.io.FileWriter

class TestInfo(val name: String, val fqName: FqName, val file: File)

class UnitTestFileWriter(
    private val flavourFolder: String,
    private val flavourName: String,
    private val generatedTestNames: MutableSet<String>
) {
    private data class GeneratedTest(
        val testName: String,
        val className: String,
        val filePath: String,
    )

    private companion object {
        const val TESTS_PER_CHUNK = 128
    }

    private val infos = arrayListOf<TestInfo>()

    fun addTests(info: List<TestInfo>) {
        infos.addAll(info)
    }

    fun generate() {
        FileWriter(File(flavourFolder, flavourName.replaceFirstChar(Char::uppercaseChar) + ".java").also { it.parentFile.mkdirs() }).use { suite ->
            val p = Printer(suite)
            val generatedTests = infos.map { info ->
                GeneratedTest(
                    testName = generateTestName(info.file.name),
                    className = info.fqName.asString(),
                    filePath = StringUtil.escapeStringCharacters(info.file.path),
                )
            }
            p.println(
                """package ${CodegenTestsOnAndroidGenerator.testClassPackage};
                |
                |import ${CodegenTestsOnAndroidGenerator.testClassPackage}.KotlinBoxResultCollector;
                |import ${CodegenTestsOnAndroidGenerator.testClassPackage}.KotlinBoxTestSuite;
                |
                |/* This class is generated by ${getMainClassName()}. DO NOT MODIFY MANUALLY */
                |public class ${flavourName.replaceFirstChar(Char::uppercaseChar)} implements KotlinBoxTestSuite {
                |
            """.trimMargin()
            )
            p.pushIndent()

            p.println("@Override")
            p.println("public void run(KotlinBoxResultCollector collector) {")
            p.pushIndent()

            for (chunkIndex in generatedTests.indices step TESTS_PER_CHUNK) {
                p.println("runChunk${chunkIndex / TESTS_PER_CHUNK}(collector);")
            }

            p.popIndent()
            p.println("}")
            p.println()

            generatedTests.chunked(TESTS_PER_CHUNK).forEachIndexed { index, chunk ->
                p.println("private void runChunk$index(KotlinBoxResultCollector collector) {")
                p.pushIndent()
                chunk.forEach { test ->
                    generateTestMethod(p, test.testName, test.className, test.filePath)
                }
                p.popIndent()
                p.println("}")
                p.println()
            }

            p.popIndent()
            p.println("}")
        }
    }

    private fun generateTestName(fileName: String): String {
        var result = NameUtils.sanitizeAsJavaIdentifier(FileUtil.getNameWithoutExtension(StringUtil.capitalize(fileName)))

        var i = 0
        while (generatedTestNames.contains(result)) {
            result += "_" + i++
        }
        generatedTestNames.add(result)
        return result
    }

    private fun generateTestMethod(p: Printer, testName: String, className: String, filePath: String) {
        p.println("System.out.println(\"Running... test$testName\");")
        p.println("{")
        p.pushIndent()
        p.println("long startedAtMs = System.currentTimeMillis();")
        p.println("try {")
        p.pushIndent()
        p.println("Object result = $className.box();")
        p.println("if (!\"OK\".equals(result)) {")
        p.pushIndent()
        p.println("throw new AssertionError(\"Expected 'OK', actual='\" + result + \"' (file: $filePath)\");")
        p.popIndent()
        p.println("}")
        p.println("long elapsedMs = System.currentTimeMillis() - startedAtMs;")
        p.println("collector.recordSuccess(\"test$testName [\" + elapsedMs + \" ms]\");")
        p.popIndent()
        p.println("} catch (Throwable t) {")
        p.pushIndent()
        p.println("long elapsedMs = System.currentTimeMillis() - startedAtMs;")
        p.println("collector.recordFailure(\"test$testName [\" + elapsedMs + \" ms]\", \"File: $filePath\", t);")
        p.popIndent()
        p.println("}")
        p.popIndent()
        p.println("}")
        p.println()
    }

}
