package org.jetbrains.kotlin.gradle.targets.js.dsl

import org.gradle.api.Task
import org.gradle.api.file.DirectoryProperty
import org.gradle.api.file.RegularFile
import org.gradle.api.file.RegularFileProperty
import org.gradle.api.provider.Provider
import org.gradle.api.tasks.InputFile
import org.gradle.api.tasks.Optional
import org.gradle.api.tasks.OutputDirectory
import org.gradle.api.tasks.OutputFile
import org.gradle.api.tasks.PathSensitive
import org.gradle.api.tasks.PathSensitivity
import org.jetbrains.kotlin.gradle.ExperimentalJsTestDsl
import org.jetbrains.kotlin.gradle.targets.js.npm.RequiresNpmDependenciesTask

/**
 * Task that bundles Kotlin/JS test compilation output for Browser runners.
 */
@ExperimentalJsTestDsl
interface BundleKotlinJsTestsTask : RequiresNpmDependenciesTask, Task {
    /**
     * Path to a JS file containing tests generated by the Kotlin / JS compiler.
     * This normally points to the output of [org.jetbrains.kotlin.gradle.targets.js.ir.KotlinJsIrLink] task.
     *
     * The main use case to change it is when the original JS file has to be post-processed.
     * It is discouraged for other Gradle Plugins to change it.
     * Only end builds should change it.
     *
     * When not set, the task will be skipped.
     */
    @get:InputFile
    @get:PathSensitive(PathSensitivity.RELATIVE)
    @get:Optional
    val testsEntryFile: RegularFileProperty

    /** The output directory for the bundle */
    @get:OutputDirectory
    val outputBundleDir: DirectoryProperty

    /**
     *  The resulted HTML file that browser will open to execute tests.
     *
     *  This HTML loads the following JS scripts:
     *  * bundle with tests and all dependencies from [testsEntryFile]
     *  * 'kotlin-test-mocha-browser-runner.js' from the kotlin-web-helpers NPM package
     *  * Mocha.js
     *  And glue JS script that configures kotlin-test-mocha-browser-runner and triggers the test run.
     *
     *  User Gradle Builds and other Gradle Plugins can use this file and [outputBundleDir] to run tests
     *  in custom tasks running user-configured browsers.
     *
     *  In the default pipeline it will be consumed by [org.jetbrains.kotlin.gradle.targets.js.testing.KotlinJsTest].
     */
    @get:OutputFile
    val testHtmlFile: Provider<RegularFile>
}
