import org.junit.jupiter.api.Tag

@Tag("custom-first-phase")
open class AbstractCustomJsCompilerFirstPhaseTest : AbstractKotlinCompilerWithTargetBackendTest(TargetBackend.JS_IR) {
    override fun configure(builder: TestConfigurationBuilder) = with(builder) {
        globalDefaults {
            // Note: Need to specify the concrete FE kind because this affects the choice of IGNORE_BACKEND_* directive.
            frontend = if (customJsCompilerSettings.defaultLanguageVersion.usesK2) FrontendKinds.FIR else FrontendKinds.ClassicFrontend
            targetPlatform = JsPlatforms.defaultJsPlatform
            dependencyKind = DependencyKind.Binary
        }

        useConfigurators(
            // These configurators are only necessary for the second compilation phase:
            ::CommonEnvironmentConfigurator,
            ::JsEnvironmentConfigurator,

            // And this configurator is necessary for the first (custom) compilation phase:
            ::CustomWebCompilerFirstPhaseEnvironmentConfigurator,
        )

        useAdditionalSourceProviders(
            ::CoroutineHelpersSourceFilesProvider,
            ::AdditionalDiagnosticsSourceFilesProvider,
        )

        facadeStep(::CustomWebCompilerFirstPhaseFacade)

        useAfterAnalysisCheckers(
            // Suppress all tests that have not been successfully compiled by the first phase.
            // And the limited number of tests where KLIBs generated by a specific compiler version
            // are known to have some problems that cause crash on the second phase.
            ::CustomKlibCompilerFirstPhaseTestSuppressor.bind(customJsCompilerSettings.version),

            // Suppress all tests that failed on the second phase if they are anyway marked as "IGNORE_BACKEND*".
            ::CustomKlibCompilerTestSuppressor,
        )

        commonConfigurationForJsBackendSecondStageTest(
            pathToTestDir = "compiler/testData/codegen/box/",
            testGroupOutputDirPrefix = "customJsCompilerFirstPhaseTest/",
            backendFacades = JsBackendFacades.WithRecompilation
        )

        setupDefaultDirectivesForFirJsBoxTest(parser = /* Does not matter */ FirParser.LightTree)

        configureJsBoxHandlers()
    }
}