/**
 * Per-property data passed from the FIR generator to the IR body filler.
 *
 * @param propertyName the simple name of the backing property
 * @param displayName  the prefix to emit before `=value`; `null` means no prefix (includeFieldNames = false)
 */
data class ToStringPropertyInfo(
    val propertyName: String,
    val displayName: String?,
)

/**
 * Declaration key for `toString()` functions generated by [ToStringGenerator].
 * One instance is created per annotated class and carries the data needed by the IR body filler.
 */
class ToStringGeneratorKey(
    val className: String,
    val propertyInfos: List<ToStringPropertyInfo>,
    val callSuper: Boolean,
) : GeneratedDeclarationKey()

val FirDeclarationOrigin.isToString get() = this is FirDeclarationOrigin.Plugin && this.key is ToStringGeneratorKey

/**
 * See https://projectlombok.org/features/ToString
 */
class ToStringGenerator(session: FirSession) : FirDeclarationGenerationExtension(session) {
    companion object {
        val TO_STRING_NAME = Name.identifier("toString")

        private val PREDICATE = DeclarationPredicate.create { annotated(listOf(LombokNames.TO_STRING)) }
    }

    override fun FirDeclarationPredicateRegistrar.registerPredicates() {
        register(PREDICATE)
    }

    private val cache: FirCache<FirClassSymbol<*>, FirNamedFunctionSymbol?, MemberGenerationContext> =
        session.firCachesFactory.createCache(::initializeToStringIfNeeded)

    override fun getCallableNamesForClass(classSymbol: FirClassSymbol<*>, context: MemberGenerationContext): Set<Name> {
        return cache.getValue(classSymbol, context)?.let { setOf(TO_STRING_NAME) } ?: emptySet()
    }

    override fun generateFunctions(callableId: CallableId, context: MemberGenerationContext?): List<FirNamedFunctionSymbol> {
        val classSymbol = context?.owner ?: return emptyList()
        return cache.getValue(classSymbol, context)?.let { listOf(it) } ?: emptyList()
    }

    private fun initializeToStringIfNeeded(classSymbol: FirClassSymbol<*>, context: MemberGenerationContext): FirNamedFunctionSymbol? {
        if (classSymbol !is FirRegularClassSymbol || classSymbol.isInterface) return null

        val toStringConfig = session.lombokService.getToString(classSymbol) ?: return null
        val declaredScope = context.declaredScope

        // Don't generate if toString() already exists in sources
        var functionWithoutParametersAlreadyExists = false
        declaredScope?.processFunctionsByName(TO_STRING_NAME) {
            if (it.valueParameterSymbols.isEmpty() && it.isRelevantForConflictsCheck) {
                functionWithoutParametersAlreadyExists = true
            }
        }
        if (functionWithoutParametersAlreadyExists) return null

        val propertyInfos = computePropertiesToInclude(toStringConfig, declaredScope)

        return createMemberFunction(
            owner = classSymbol,
            key = ToStringGeneratorKey(
                className = classSymbol.classId.shortClassName.asString(),
                propertyInfos = propertyInfos,
                callSuper = toStringConfig.callSuper == CallSuperMode.Call,
            ),
            name = TO_STRING_NAME,
            returnType = StandardTypes.String,
        ) {
            modality = Modality.OPEN
            status {
                isOverride = true
            }
            withGeneratedDefaultBody()
        }.symbol
    }

    private fun computePropertiesToInclude(
        toStringConfig: ConeLombokAnnotations.ToString,
        declaredScope: FirClassDeclaredMemberScope?,
    ): List<ToStringPropertyInfo> {
        return buildList {
            declaredScope?.processAllProperties { variableSymbol ->
                val property = variableSymbol as? FirPropertySymbol ?: return@processAllProperties

                val propertyIdentifier = property.name.identifier

                if (property.findAnnotationOnPropertyOrField(LombokNames.TO_STRING_EXCLUDE_ID, session) != null ||
                    propertyIdentifier in toStringConfig.excludeFields
                ) {
                    return@processAllProperties
                }

                val toStringIncludeAnnotation = property.findAnnotationOnPropertyOrField(LombokNames.TO_STRING_INCLUDE_ID, session)

                // TODO: include parameterless methods and computed properties (no backing field) if user explicitly opts them in (mirror Lombok behavior for methods).
                @OptIn(SymbolInternals::class) // It's needed because `hasBackingField` on symbol requires `BODY_RESOLVE` phase, but the currenct phase might be less
                if ((!property.fir.hasBackingField || toStringConfig.onlyExplicitlyIncluded) && toStringIncludeAnnotation == null) {
                    return@processAllProperties
                }

                val displayName = if (toStringConfig.includeFieldNames) {
                    val customName = toStringIncludeAnnotation?.let {
                        it.findArgumentByName(INCLUDE_NAME)
                            ?.let { arg -> (arg as? FirLiteralExpression)?.value as? String }
                            ?.takeIf { name -> name.isNotEmpty() }
                    }
                    customName ?: propertyIdentifier
                } else {
                    null
                }

                val rank = toStringIncludeAnnotation
                    ?.findArgumentByName(INCLUDE_RANK)
                    ?.let { arg ->
                        val literal = arg as? FirLiteralExpression ?: return@let null
                        (literal.value as? Int) ?: (literal.value as? Long)?.toInt()
                    }
                    ?: 0

                add(ToStringPropertyInfo(propertyIdentifier, displayName) to rank)
            }
            sortByDescending { it.second }
        }.map { it.first }
    }
}
