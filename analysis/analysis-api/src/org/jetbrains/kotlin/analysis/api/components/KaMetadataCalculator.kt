package org.jetbrains.kotlin.analysis.api.components

import com.google.common.collect.Multimap
import com.intellij.psi.PsiElement
import org.jetbrains.kotlin.analysis.api.KaNonPublicApi
import org.jetbrains.kotlin.psi.KtClassOrObject
import org.jetbrains.kotlin.psi.KtElement
import org.jetbrains.kotlin.psi.KtFile

@KaNonPublicApi
public interface KaMetadataCalculator {
    /**
     * Calculates metadata that would be generated by the compiler if this class was compiled to a JVM class file.
     *
     * @param mapping A map containing the light elements (`KtLightElement`) for each callable declaration in this class.
     */
    @KaNonPublicApi
    public fun KtClassOrObject.calculateMetadata(mapping: Multimap<KtElement, PsiElement>): Metadata

    /**
     * Calculates metadata that would be generated by the compiler if this file was compiled to a JVM class file.
     *
     * @param mapping A map containing the light elements (`KtLightElement`) for each callable declaration in this file.
     */
    @KaNonPublicApi
    public fun KtFile.calculateMetadata(mapping: Multimap<KtElement, PsiElement>): Metadata
}
