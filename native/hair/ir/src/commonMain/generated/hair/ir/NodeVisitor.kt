package hair.ir

import hair.ir.nodes.*

abstract class NodeVisitor<R> {
    abstract fun visitNode(node: Node): R

    open fun visitNoValue(node: NoValue): R = visitNode(node)
    open fun visitUse(node: Use): R = visitBlockBody(node)
    open fun visitBlockEntry(node: BlockEntry): R = visitNode(node)
    open fun visitControlled(node: Controlled): R = visitNode(node)
    open fun visitBlockBody(node: BlockBody): R = visitControlled(node)
    open fun visitBlockBodyWithException(node: BlockBodyWithException): R = visitBlockBody(node)
    open fun visitBlockEnd(node: BlockEnd): R = visitControlled(node)
    open fun visitReturn(node: Return): R = visitBlockEnd(node)
    open fun visitGoto(node: Goto): R = visitBlockEnd(node)
    open fun visitIfProjection(node: IfProjection): R = visitNode(node)
    open fun visitIfTrue(node: If.True): R = visitIfProjection(node)
    open fun visitIfFalse(node: If.False): R = visitIfProjection(node)
    open fun visitIf(node: If): R = visitBlockEnd(node)
    open fun visitThrow(node: Throw): R = visitBlockEnd(node)
    open fun visitUnwind(node: Unwind): R = visitNode(node)
    open fun visitVarOp(node: VarOp): R = visitBlockBody(node)
    open fun visitReadVar(node: ReadVar): R = visitVarOp(node)
    open fun visitAssignVar(node: AssignVar): R = visitVarOp(node)
    open fun visitPhi(node: Phi): R = visitNode(node)
    open fun visitParam(node: Param): R = visitNode(node)
    open fun visitCatch(node: Catch): R = visitNode(node)
    open fun visitBinaryOp(node: BinaryOp): R = visitNode(node)
    open fun visitConstI(node: ConstI): R = visitNode(node)
    open fun visitBinaryOpI(node: BinaryOpI): R = visitBinaryOp(node)
    open fun visitAddI(node: AddI): R = visitBinaryOpI(node)
    open fun visitSubI(node: SubI): R = visitBinaryOpI(node)
    open fun visitMulI(node: MulI): R = visitBinaryOpI(node)
    open fun visitDivI(node: DivI): R = visitBinaryOpI(node)
    open fun visitRemI(node: RemI): R = visitBinaryOpI(node)
    open fun visitConstL(node: ConstL): R = visitNode(node)
    open fun visitBinaryOpL(node: BinaryOpL): R = visitBinaryOp(node)
    open fun visitAddL(node: AddL): R = visitBinaryOpL(node)
    open fun visitSubL(node: SubL): R = visitBinaryOpL(node)
    open fun visitMulL(node: MulL): R = visitBinaryOpL(node)
    open fun visitDivL(node: DivL): R = visitBinaryOpL(node)
    open fun visitRemL(node: RemL): R = visitBinaryOpL(node)
    open fun visitConstF(node: ConstF): R = visitNode(node)
    open fun visitBinaryOpF(node: BinaryOpF): R = visitBinaryOp(node)
    open fun visitAddF(node: AddF): R = visitBinaryOpF(node)
    open fun visitSubF(node: SubF): R = visitBinaryOpF(node)
    open fun visitMulF(node: MulF): R = visitBinaryOpF(node)
    open fun visitDivF(node: DivF): R = visitBinaryOpF(node)
    open fun visitRemF(node: RemF): R = visitBinaryOpF(node)
    open fun visitConstD(node: ConstD): R = visitNode(node)
    open fun visitBinaryOpD(node: BinaryOpD): R = visitBinaryOp(node)
    open fun visitAddD(node: AddD): R = visitBinaryOpD(node)
    open fun visitSubD(node: SubD): R = visitBinaryOpD(node)
    open fun visitMulD(node: MulD): R = visitBinaryOpD(node)
    open fun visitDivD(node: DivD): R = visitBinaryOpD(node)
    open fun visitRemD(node: RemD): R = visitBinaryOpD(node)
    open fun visitNew(node: New): R = visitBlockBody(node)
    open fun visitPinnedMemoryOp(node: PinnedMemoryOp): R = visitBlockBody(node)
    open fun visitPinnedInstanceFieldOp(node: PinnedInstanceFieldOp): R = visitPinnedMemoryOp(node)
    open fun visitPinnedGlobalFieldOp(node: PinnedGlobalFieldOp): R = visitPinnedMemoryOp(node)
    open fun visitReadFieldPinned(node: ReadFieldPinned): R = visitPinnedInstanceFieldOp(node)
    open fun visitReadGlobalPinned(node: ReadGlobalPinned): R = visitPinnedGlobalFieldOp(node)
    open fun visitWriteField(node: WriteField): R = visitPinnedInstanceFieldOp(node)
    open fun visitWriteGlobal(node: WriteGlobal): R = visitPinnedGlobalFieldOp(node)
    open fun visitTypeCheck(node: TypeCheck): R = visitNode(node)
    open fun visitIsInstanceOf(node: IsInstanceOf): R = visitTypeCheck(node)
    open fun visitCast(node: Cast): R = visitTypeCheck(node)
    open fun visitValueProxy(node: ValueProxy): R = visitBlockBody(node)
    open fun visitStaticCall(node: StaticCall): R = visitBlockBodyWithException(node)
}
