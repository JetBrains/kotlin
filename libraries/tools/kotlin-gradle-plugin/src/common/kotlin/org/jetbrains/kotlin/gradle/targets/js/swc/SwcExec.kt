/**
 * A Gradle task for transpiling generated by the compiler JS-code using SWC (Speedy Web Compiler) CLI.
 *
 * @see [Speedy Web Compiler](https://swc.rs/)
 */
@CacheableTask
internal abstract class SwcExec
@Inject
internal constructor(
    @Internal
    @Transient
    val compilation: KotlinJsIrCompilation
) : AbstractExecTask<SwcExec>(SwcExec::class.java) {
    @get:Input
    val mode: Property<Mode> = project.objects.property<Mode>().convention(Mode.DEVELOPMENT)

    @get:Nested
    abstract val config: KotlinSwcConfig

    @get:Internal
    abstract val inputFilesDirectory: DirectoryProperty

    private val npmProjectDir by project.provider { compilation.npmProject.dir }

    @get:Internal
    internal abstract val npmToolingEnvDir: DirectoryProperty

    @get:OutputDirectory
    @get:Optional
    abstract val outputDirectory: DirectoryProperty

    @get:OutputFile
    open val configFile: Provider<File> =
        npmProjectDir.map { it.asFile.resolve(".swcrc") }

    override fun exec() {
        configFile.get().writeText(json(config.toConfigMap()))

        workingDir = inputFilesDirectory.get().asFile
        args = SwcConfig.getArgumentsWhen(
            inputDirectory = "./",
            outputDirectory = outputDirectory.get().asFile.absolutePath,
            configPath = configFile.get().absolutePath,
            fileExtension = config.fileExtension.get(),
            environmentCode = mode.get().code
        )
        super.exec()
    }

    companion object {
        fun register(
            compilation: KotlinJsIrCompilation,
            name: String,
            configuration: SwcExec.() -> Unit = {},
        ): TaskProvider<SwcExec> {
            val target = compilation.target
            val project = target.project
            val swc = SwcPlugin.apply(project)
            return project.registerTask(name, listOf(compilation)) {
                it.executable = swc.requireConfigured().executable
                it.dependsOn(swc.setupTaskProvider)
                it.dependsOn(compilation.compileTaskProvider)
                it.configuration()
            }
        }
    }
}
