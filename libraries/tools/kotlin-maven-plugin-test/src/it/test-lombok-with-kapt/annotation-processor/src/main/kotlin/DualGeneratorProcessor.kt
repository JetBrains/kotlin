package example

import java.io.File
import javax.annotation.processing.AbstractProcessor
import javax.annotation.processing.RoundEnvironment
import javax.lang.model.SourceVersion
import javax.lang.model.element.ElementKind
import javax.lang.model.element.TypeElement
import javax.tools.StandardLocation

/**
 * Annotation processed by [DualGeneratorProcessor].
 */
annotation class GenerateCompanion

/**
 * An annotation processor that generates both a Java helper class and a Kotlin extension file
 * for each class annotated with [GenerateCompanion].
 *
 * For a class `Foo` in package `bar`:
 * - Generates `bar/FooHelper.java` — a Java factory with a static `create()` method
 * - Generates `bar/FooExtensions.kt` — a Kotlin file with a `describe()` extension function
 */
class DualGeneratorProcessor : AbstractProcessor() {
    private companion object {
        const val KAPT_KOTLIN_GENERATED_OPTION = "kapt.kotlin.generated"
    }

    override fun process(annotations: MutableSet<out TypeElement>?, roundEnv: RoundEnvironment): Boolean {
        val elements = roundEnv.getElementsAnnotatedWith(GenerateCompanion::class.java)
        val kotlinGenerated = processingEnv.options[KAPT_KOTLIN_GENERATED_OPTION]

        for (element in elements) {
            if (element.kind != ElementKind.CLASS) continue

            val packageName = processingEnv.elementUtils.getPackageOf(element).qualifiedName.toString()
            val simpleName = element.simpleName.toString()

            // Generate a Java helper class
            val javaClassName = "${simpleName}Helper"
            val javaFile = processingEnv.filer.createSourceFile("$packageName.$javaClassName", element)
            javaFile.openWriter().buffered().use {
                it.appendLine("package $packageName;")
                it.appendLine()
                it.appendLine("/** Generated by DualGeneratorProcessor for {@link $simpleName}. */")
                it.appendLine("public class $javaClassName {")
                it.appendLine("    public static $simpleName create() {")
                it.appendLine("        return new $simpleName();")
                it.appendLine("    }")
                it.appendLine("}")
            }

            // Generate a Kotlin extension file
            if (kotlinGenerated != null) {
                val packageDir = packageName.replace('.', File.separatorChar)
                val dir = File(kotlinGenerated, packageDir).also { it.mkdirs() }
                File(dir, "${simpleName}Extensions.kt").writer().buffered().use {
                    it.appendLine("package $packageName")
                    it.appendLine()
                    it.appendLine("/** Generated by DualGeneratorProcessor for [$simpleName]. */")
                    it.appendLine("fun $simpleName.describe(): String = \"$simpleName(generated)\"")
                }
            }
        }

        return true
    }

    override fun getSupportedSourceVersion(): SourceVersion = SourceVersion.latestSupported()
    override fun getSupportedAnnotationTypes(): Set<String> = setOf(GenerateCompanion::class.java.name)
    override fun getSupportedOptions(): Set<String> = emptySet()
}